# Break
for i in range(0, 10):  # 0 to 9 -> 10 times
    print(i)
    if i == 5:
      break


# Three table formula

#  | i | condition | o/p
#  | 0 | 0 ==5--> false | o/p=0
#  | 1 | 1 ==5--> false | o/p=1
#  | 2 | 2 ==5--> false | o/p=2
#  | 3 | 3 ==5--> false | o/p=3
#  | 4 | 4 ==5--> false | o/p=4
#  | 5 | 5 ==5--> false | o/p=Break and Exit the loop
