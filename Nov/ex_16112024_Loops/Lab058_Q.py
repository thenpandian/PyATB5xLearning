# % Modulus operator- will give th Reminder

for i in range(101):  # 0  to 100
    if i % 2 == 0:
        print(i)

# Three table formula

#   | i | condition | o/p
#   | 0 | 0%2==0 True |o/p-> 0
#   | 1 | 1%2==0 False |o/p-> Nothing will be printed
#   |2  |2%2==0 True    |o/P ->2
#   | 3 | 3%2==0 False |o/p-> Nothing will be printed
#   |4 |4%2==0 True    |o/P ->4
