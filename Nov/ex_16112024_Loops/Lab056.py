#  | i | condition | o/p
#  | 0 | 0 ==6--> false | o/p=No O/P
#  | 1 | 1 ==6--> false | o/p=No O/P
#  | 2 | 2 ==6--> false | o/p=No O/P
#  | 3 | 3 ==6--> false | o/p=No O/P
#  | 4 | 4 ==6--> false | o/p=No O/P
