for i in range(0,10,1):
    if i ==6 or i == 5:
        print(i)
    else :
        pass    #pass is  a placeholder statement that does nothing
    # it's used when a statement is syntactically required but no action is  needed.

# Three table formula

#  | i | condition | o/p
#  | 0 | 0 ==6--> false | o/p=No Pass (Nothing will be printed)
#  | 1 | 1 ==6--> false | o/p=No Pass (Nothing will be printed)
#  | 2 | 2 ==6--> false | o/p=No Pass (Nothing will be printed)
#  | 3 | 3 ==6--> false | o/p=No Pass (Nothing will be printed)
#  | 4 | 4 ==6--> false | o/p=No Pass (Nothing will be printed)
#  | 5 | 5 ==6--> true | o/p= 5
#  | 6 | 6 ==6--> true | o/p= 6