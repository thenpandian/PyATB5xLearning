for i in range(0, 10):  # 0 to 9 -> 10 times
    if i == 5:
        print("FIVE")
    else:
        print(i)


# Three table formula

#  | i | condition | o/p
#  | 0 | 0 ==5--> false | o/p=0
#  | 1 | 1 ==5--> false | o/p=1
#  | 2 | 2 ==5--> false | o/p=2
#  | 3 | 3 ==5--> false | o/p=3
#  | 4 | 4 ==5--> false | o/p=4
#  | 5 | 5 ==5--> True | o/p=FIVE
#  | 6 | 6 ==5--> false | o/p=6
#  | 9 | 9 ==5--> false | o/p=9
#  | 10 | 10 ==5--> false | o/p=Exit the loop

