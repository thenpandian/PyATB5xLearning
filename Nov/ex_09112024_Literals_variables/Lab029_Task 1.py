#Take a 3 input  from the user
# perform sum, sub, mul, div

num1 = int(input("Enter the number 1 : "))
num2 = int(input("Enter the number 2 : "))
num3 = int(input("Enter the number 3 : "))

sum = num1 + num2 + num3
sub = num1 - num2 - num3
mul = num1 * num2 * num3
div = num1 / num2 / num3

print (" Sum is ",sum)
print (" Sub is ",sub)
print (" Mul is ",mul)
print (" Div is ",div)